function class = ClassifyWithMinMax(features, selectedFeatures)
  % Parameters
  
  NICE = 0.1;
  
  minFeatures = [0.0067,...
                 2.1858,...
                 0.4757,...
                 0.2224,...
                 10.5257,...
                 21.0515,...
                 31.5772,...
                 31.5772,...
                 63.1545,...
                 42.1030];
               
   maxFeatures = [0.0099,...
                  3.7748,...
                  0.8813,...
                  0.7253,...
                  16.0486,...
                  32.0971,...
                  48.1457,...
                  48.1457,...
                  96.2914,...
                  64.1942];
  
  minFeatures = (1-NICE)*minFeatures(selectedFeatures);
  maxFeatures = (1+NICE)*maxFeatures(selectedFeatures);
  nCases = size(features,1);
  
  % Classify hands
  isAllowed = (features > (ones(nCases,1) * minFeatures)) &...
          (features < (ones(nCases,1) * maxFeatures));
  class = all(isAllowed,2);
    
end